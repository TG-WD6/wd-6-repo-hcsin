package hcsin.variableAndOperators;

public class Opdracht6 {
    public static void main(String[] args) {
        int cats = 4;
        int dogs = 5;
        System.out.println(cats + dogs);
        System.out.println(cats - dogs);
        System.out.println(cats * dogs);
        System.out.println(cats / dogs);
        System.out.println(cats % dogs);
        System.out.println(cats > dogs);
    }
}
